const mongoose = require('mongoose')
const Schema = mongoose.Schema
const filmUsersSchema = new Schema({
  name: { // 姓名
    type: String,
    require: true
  },
  gender: { // 性别
    type: String,
    require: true
  },
  age: { // 年龄
    type: Number
  },
  code: { // 身份证号
    type: String
  },
  remark: { // 备注
    type: String
  }
})
